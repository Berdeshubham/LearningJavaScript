let animal = prompt("lion,tiger,lizard")
switch (animal) {
    case "lion":
        console.log("The animal is lion")
        break;

    case "tiger":
        console.log("The animal is tiger")
        break;
    
    case "lizard":
        console.log("The animal is lizard")
        break;
            
    default:
        console.log("invalid name")
        break;
}